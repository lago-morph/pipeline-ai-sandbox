class TestExports:
    def test_known_constants(self):
        assert envelopes.PROTOCOL_VERSION == 1
        assert envelopes.KIND_REQUEST == "batch-job-request"
        assert envelopes.KIND_ACK == "agent-ack"
        assert set(envelopes.TERMINAL_STATUSES) == {
            "completed",
            "error",
            "parse_error",
        }

    def test_all_lists_public_names(self):
        for name in envelopes.__all__:
            assert hasattr(envelopes, name), f"__all__ references missing {name!r}"
