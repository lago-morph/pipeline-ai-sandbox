"""Unit tests for the envelopes module."""
from __future__ import annotations

import json

import pytest

import envelopes


VALID_SHA = "a" * 40


def _request(**overrides):
    base = dict(
        command="echo",
        args={"message": "hi"},
        branch="main",
        commit_sha=VALID_SHA,
        subagent_id="harness-batch-job",
        submitted_at="2026-05-17T00:00:00Z",
    )
    base.update(overrides)
    return base


# ---------------------------------------------------------------------------
# build_request
# ---------------------------------------------------------------------------
class TestBuildRequest:
    def test_sets_required_markers(self):
        env = envelopes.build_request(**_request())
        assert env["protocol_version"] == 1
        assert env["kind"] == "batch-job-request"

    def test_copies_args(self):
        original = {"message": "hi"}
        env = envelopes.build_request(**_request(args=original))
        # Returned dict is decoupled from caller's dict.
        original["message"] = "mutated"
        assert env["args"] == {"message": "hi"}

    def test_handles_none_args(self):
        env = envelopes.build_request(**_request(args=None))
        assert env["args"] == {}

    def test_no_run_status_initially(self):
        env = envelopes.build_request(**_request())
        assert "run_status" not in env

    def test_rejects_empty_command(self):
        with pytest.raises(ValueError, match="command"):
            envelopes.build_request(**_request(command=""))

    def test_rejects_empty_branch(self):
        with pytest.raises(ValueError, match="branch"):
            envelopes.build_request(**_request(branch=""))

    def test_rejects_short_sha(self):
        with pytest.raises(ValueError, match="commit_sha"):
            envelopes.build_request(**_request(commit_sha="abc123"))

    def test_rejects_non_string_sha(self):
        with pytest.raises(ValueError, match="commit_sha"):
            envelopes.build_request(**_request(commit_sha=None))

    def test_rejects_empty_subagent_id(self):
        with pytest.raises(ValueError, match="subagent_id"):
            envelopes.build_request(**_request(subagent_id=""))

    def test_rejects_empty_submitted_at(self):
        with pytest.raises(ValueError, match="submitted_at"):
            envelopes.build_request(**_request(submitted_at=""))


